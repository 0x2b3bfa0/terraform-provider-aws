// +build ignore

package main

import (
	"bytes"
	"go/format"
	"log"
	"os"
	"sort"
	"strings"
	"text/template"
)

// "_test" is required for the functions to be compiled in the correct package
const filename = `cloudformation_sweepers_gen_test.go`

var resourceNames = []string{
	"StackSet",
}

type TemplateData struct {
	Package       string
	ServiceName   string
	ResourceNames []string
}

func main() {
	// Always sort to reduce any potential generation churn
	sort.Strings(resourceNames)

	destinationPackage := os.Getenv("GOPACKAGE")
	if destinationPackage == "" {
		log.Fatal("error: required environment variable GOPACKAGE not defined")
	}

	templateData := TemplateData{
		Package:       destinationPackage,
		ServiceName:   "cloudformation",
		ResourceNames: resourceNames,
	}
	templateFuncMap := template.FuncMap{
		"Title": strings.Title,
	}

	tmpl, err := template.New("sweepers").Funcs(templateFuncMap).Parse(sweepersBody)

	if err != nil {
		log.Fatalf("error parsing template: %s", err)
	}

	var buffer bytes.Buffer
	err = tmpl.Execute(&buffer, templateData)

	if err != nil {
		log.Fatalf("error executing template: %s", err)
	}

	generatedFileContents, err := format.Source(buffer.Bytes())

	if err != nil {
		log.Fatalf("error formatting generated file: %s", err)
	}

	f, err := os.Create(filename)

	if err != nil {
		log.Fatalf("error creating file (%s): %s", filename, err)
	}

	defer f.Close()

	_, err = f.Write(generatedFileContents)

	if err != nil {
		log.Fatalf("error writing to file (%s): %s", filename, err)
	}
}

var sweepersBody = `
// Code generated by cloudformation/generators/sweepers/main.go; DO NOT EDIT.

package {{ .Package }}

import (
	"fmt"
	"log"

	"github.com/aws/aws-sdk-go/aws"
	"github.com/aws/aws-sdk-go/service/{{ .ServiceName }}"
	"github.com/hashicorp/go-multierror"
	"github.com/terraform-providers/terraform-provider-aws/aws/internal/service/{{ .ServiceName }}/lister"
)

{{- $serviceName := .ServiceName | Title }}
{{- range .ResourceNames }}

{{ $fullResourceName := printf "%s %s" $serviceName . }}
{{ $fullResourceNamePlural := printf "%s %ss" $serviceName . }}

func testSweep{{ $serviceName }}{{ . }}s(region string) error {
	conn, err := shared{{ $serviceName }}ClientForRegion(region)
	if err != nil {
		return err
	}

	var sweeperErrs *multierror.Error

	err = lister.ListAllStackSetsPages(conn, func(page *cloudformation.ListStackSetsOutput, lastPage bool) bool {
		if page == nil {
			return !lastPage
		}

		for _, stackSet := range page.Summaries {
			name := aws.StringValue(stackSet.StackSetName)

			log.Printf("[INFO] Deleting {{ $fullResourceName }}: %s", name)
			err := deleteCloudFormationStackSet(conn, deleteCloudFormationStackSetInputFromAPIResource(stackSet))
			if err != nil {
				sweeperErrs = multierror.Append(sweeperErrs, fmt.Errorf("error deleting {{ $fullResourceName }} (%s): %w", name, err))
				continue
			}
		}

		return !lastPage
	})

	if testSweepSkipSweepError(err) {
		log.Printf("[WARN] Skipping {{ $fullResourceName }} sweeper for %q: %s", region, err)
		return sweeperErrs.ErrorOrNil() // In case we have completed some pages, but had errors
	}

	if err != nil {
		sweeperErrs = multierror.Append(sweeperErrs, fmt.Errorf("error listing {{ $fullResourceNamePlural }}: %w", err))
	}

	return sweeperErrs.ErrorOrNil()
}
{{- end }}
`
